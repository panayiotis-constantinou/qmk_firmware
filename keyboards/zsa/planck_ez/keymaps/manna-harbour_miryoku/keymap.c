void set_layer_color(int layer) {
    for (int i = 0; i < RGB_MATRIX_LED_COUNT; i++) {
        HSV hsv = {
            .h = pgm_read_byte(&ledmap[layer][i][0]),
            .s = pgm_read_byte(&ledmap[layer][i][1]),
            .v = pgm_read_byte(&ledmap[layer][i][2]),
        };
        if (!hsv.h && !hsv.s && !hsv.v) {
            rgb_matrix_set_color(i, 0, 0, 0);
        } else {
            RGB rgb = hsv_to_rgb_with_value(hsv);
            rgb_matrix_set_color(i, rgb.r, rgb.g, rgb.b);
        }
    }
}

bool rgb_matrix_indicators_user(void) {
    if (!keyboard_config.disable_layer_led) {
        switch (biton32(layer_state)) {
            case 0: set_layer_color(0); break;  // U_BASE
            case 4: set_layer_color(4); break;  // U_NAV
            case 5: set_layer_color(5); break;  // U_MOUSE
            case 6: set_layer_color(6); break;  // U_MEDIA
            case 7: set_layer_color(7); break;  // U_NUM
            case 8: set_layer_color(8); break;  // U_SYM
            case 9: set_layer_color(9); break;  // U_FUN
            default:
                if (rgb_matrix_get_flags() == LED_FLAG_NONE) {
                    rgb_matrix_set_color_all(0, 0, 0);
                }
        }
    } else {
        if (rgb_matrix_get_flags() == LED_FLAG_NONE) {
            rgb_matrix_set_color_all(0, 0, 0);
        }
    }
    if (capslock_active && biton32(layer_state) == 4) {  // U_NAV
        RGB rgb = hsv_to_rgb_with_value((HSV){245, 233, 224});
        rgb_matrix_set_color(19, rgb.r, rgb.g, rgb.b);
    }
    if (scrolllock_active && biton32(layer_state) == 9) {  // U_FUN
        RGB rgb = hsv_to_rgb_with_value((HSV){92, 218, 204});
        rgb_matrix_set_color(16, rgb.r, rgb.g, rgb.b);
    }
    if (mouse_jiggler_is_enabled()) {
        RGB rgb = hsv_to_rgb_with_value((HSV){85, 255, 220});
        rgb_matrix_set_color(36, rgb.r, rgb.g, rgb.b);
    }
    return true;
}
